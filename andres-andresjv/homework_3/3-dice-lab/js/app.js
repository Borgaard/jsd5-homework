//on Button click call set dice image function
document.getElementById("roll-dice").onclick=function(){
  setDiceImage("first-die");
  setDiceImage("second-die");

}


//Receive the class name, call the random number function and set dice image
function setDiceImage(clName){
    document.getElementById(clName).className = "dice dice-"+randNum();
}


// Rando Number function
function randNum () {
    var min = 1;
    var max = 6;
    min = Math.ceil(min);
    max = Math.floor(max);

    return Math.floor(Math.random() * (max - min + 1)) + min;
}


